</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; •&nbsp;&nbsp;&nbsp;	Generali differentiates itself from other insurance providers by offering excellent service to agents. In an effort to improve its service offerings and streamline processes at the home office, the company sought an enterprise system to implement new, improved processes</p>
</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; •&nbsp;&nbsp;&nbsp;	In addition, many of the processes being used by the sales force were outdated. Rather than putting old data and processes on top of a new system, the IT team intended to develop entirely new, automated processes to replace those bogged down by paper</p>
</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; •&nbsp;&nbsp;&nbsp;	Seeing the specific transactions, what the decisions were, what the offers were, and how they were negotiated, especially after a long time was very difficult</p>
</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; •&nbsp;&nbsp;&nbsp;	Agents would submit an insurance application, and then have no idea where it was in the process</p>
</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">➢&nbsp;&nbsp;&nbsp; SOLUTION:</p>
</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; •&nbsp;&nbsp;&nbsp;	Data entry workers and dispatchers are responsible for handling incoming paper documents and bringing them inside the domain, performing scanning and other functions of different documents</p>
</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; •&nbsp;&nbsp;&nbsp;	Claims handlers can include doctors, claims experts, or companies involved in information exchange; these users, who become part of the process on an as-needed basis, help insured parties fill out paperwork, provide photographs, and communicate with Generali</p>
</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; •&nbsp;&nbsp;&nbsp;	As soon as the application is approved, it becomes an insurance policy that contains terms and conditions, letters, forms, and issued bills, all kept together in the system</p>
</div><!-- /.row  --> 
<div class="row loaders">
    <p class="callout">&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; •&nbsp;&nbsp;&nbsp;	Another case consists of claims brought against a policy that has been created. This entity may contain reports from experts, invoices for compensation, and records of checks that have been issued. The system knows who the immediate supervisor of each claim handler is, so it sends dependency notifications stating the agent wants to make a payment, to get approval</p>
</div><!-- /.row  --> 
<div class="row loaders">
